import mongoose from "mongoose";
import {  usersDB } from "../dbConnections.js";

const noticeSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
    trim: true
  },
  content: {
    type: String,
    required: true,
    trim: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

export default usersDB.model("notices", noticeSchema);
 


